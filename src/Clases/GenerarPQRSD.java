package Clases;

public class GenerarPQRSD {
	
	public String Placa;
	public String Empresa;
	public String DescripcionPQRSD;
	public boolean Peticiones;
	public boolean Quejas;
	public boolean Reclamos;
	public boolean Sugerencias;
	public boolean Denuncias;
	public String Usuario;
	
	public GenerarPQRSD(String Placa, String Empresa, String DescripcionPQRSD, boolean Peticiones, boolean Quejas, boolean Reclamos, boolean Sugerencias, boolean Denuncias, String Usuario) {

		this.Placa="Placa";
		this.Empresa="Empresa";
		this.DescripcionPQRSD="DescripcionPQRSD";
		this.Peticiones="Peticiones" != null;
		this.Quejas="Quejas" != null;
		this.Reclamos="Reclamos" != null;
		this.Sugerencias="Sugerencias" != null;
		this.Denuncias="Denuncias" != null;
		this.Usuario="Usuario";
				
	}

}
