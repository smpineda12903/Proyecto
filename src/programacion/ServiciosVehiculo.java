package programacion;

public class ServiciosVehiculo {

//ATRIBUTOS
public boolean aireacondicionado;
public boolean capacidad;
public boolean dosconductores;
public boolean dospisos;
public boolean gps;
public boolean vip;
public boolean pantallasindividuales;
public boolean tv;
public boolean wifi;

//CONSTRUCTORES
public ServiciosVehiculo (boolean aireacondicionado, boolean capacidad, boolean dosconductores, boolean dospisos, boolean gps, boolean vip, boolean pantallasindividuales, boolean tv, boolean wifi) {
	this.aireacondicionado = aireacondicionado;
	this.capacidad = capacidad;
	this.dosconductores = dosconductores;
	this.dospisos = dospisos;
	this.gps = gps;
	this.vip = vip;
	this.pantallasindividuales = pantallasindividuales;
	this.tv = tv;
	this.wifi = wifi;
	
}	
}